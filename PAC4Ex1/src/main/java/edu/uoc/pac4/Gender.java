package edu.uoc.pac4;

/**
 * The {@code Gender} enum represents the gender of a person, with possible values being {@code MALE} and {@code FEMALE}.
 * This enum is commonly used to specify the gender of a {@link Superstar}.
 *
 * @author Marc Servat
 * @version 1.0
 * @since 2023-12-11
 */
public enum Gender {
    /**
     * Represents the male gender.
     */
    MALE,
    /**
     * Represents the female gender.
     */
    FEMALE,
}

